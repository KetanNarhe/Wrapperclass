package wrapper;

public class WrapperBoxing {

	public static void main(String[] args) {
		
		int i=100;
		
		Integer li=new Integer(i);
		System.out.println(li);
		
		boolean b=true;
		Boolean b1=new Boolean(b);
		System.out.println(b1);
		
		Double d=10.20;
		Double d1=new Double(d);
		System.out.println(d1);

	}

}
